import java.util.*;

public class Main2 {


    public static void main(String[] args) {
        
        String name= "Lambodar";

        System.out.println(name.charAt(0));



        System.out.println("Printing all string characters : " + '\n');


        for(int i = 0; i< name.length() ; i++){

            System.out.print(name.charAt(i) );


        }
    }
    
}
